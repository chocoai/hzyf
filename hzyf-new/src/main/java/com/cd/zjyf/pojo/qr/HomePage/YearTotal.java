public class YearTotal {

    private String zl; //今年总重量
    private String cs; //今年总次数
    private String avgZL;//日均重量

    public String getZl() {
        return zl;
    }

    public void setZl(String zl) {
        this.zl = zl;
    }

    public String getCs() {
        return cs;
    }

    public void setCs(String cs) {
        this.cs = cs;
    }

    public String getAvgZL() {
        return avgZL;
    }

    public void setAvgZL(String avgZL) {
        this.avgZL = avgZL;
    }
}
